main() {
  //Operadores de Atribuição (binário/infix)

  int a = 2;
  print(a);
  a = a + 3;
  print(a);

  double b = 5;
  print(b);
  b += 7;
  b -= 1;
  b *= 4;
  b /= 2;

  print(b);

  //Operadores de Relacionais (binário/infix)

  print(5 < 3);
  print(6 > 8);
  print(7 != 7);
  print(7 >= 7);
  print(6 <= 8);
  print(7 == 7);
}
